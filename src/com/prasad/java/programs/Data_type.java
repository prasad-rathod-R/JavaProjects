package com.prasad.java.programs;

public class Data_type {
	public static void main(String[] args) {
		int x = 9;
		System.out.println("This is integer value " + x);

		char division = 'A';
		System.out.println("This is character value " + division);

		short x1 = 999;
		System.out.println("this is short data type " + x1);

		long x2 = 99999999999L;
		System.out.println("this is long data type " + x2);

		byte x3 = 99;
		System.out.println("this is byte data type " + x3);

		double x4 = 99.99d;
		System.out.println("this is double datatype " + x4);

		boolean isAvailable = true;
		System.out.println("this is boolean data type " + isAvailable);
	}
}
